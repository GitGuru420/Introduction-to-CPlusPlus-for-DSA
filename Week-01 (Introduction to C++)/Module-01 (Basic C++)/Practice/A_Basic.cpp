#include <iostream>
using namespace std;

int main() {
    // print in C++
    cout << "Hello C++ Programming Language" << endl;

    // input & output in C++
    int number;
    cout << "Enter an integer number: ";
    cin >> number;
    cout << "The entered number is: " << number << endl;

    // typecasting in C++
    char c;
    cout << "Enter any character from A to Z: ";
    cin >> c;
    cout << "The entered character ASCII value is: " << (int)c << endl;

    return 0;
}